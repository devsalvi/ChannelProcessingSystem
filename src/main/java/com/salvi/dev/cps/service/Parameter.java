package com.salvi.dev.cps.service;

/**
 * Parameter class to store the values of m and c
 */
public class Parameter {
    double m;
    double c;

    public double getM() {
        return m;
    }

    public void setM(double m) {
        this.m = m;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }
}
